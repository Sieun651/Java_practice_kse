// 관계연산자
// -> 왼쪽 값과 오른쪽 값의 관계를 따져서 true 또는 false 값을 구하는 명령어
// 5 > 7 : false
// 5 < 7 : true
// 5 >= 7 : false
// 5 <= 7 : true
// 5 == 7 : false	, 같은지 검사
// 5 != 7 : true	, 같지 않은지 검사
public class Exam6 {
	public static void main(String[] args) {
		System.out.println(5>7);
		System.out.println(5<7);
		System.out.println(5>=7);
		System.out.println(5<=7);
		System.out.println(5==7);
		System.out.println(5!=7);
	}
}
